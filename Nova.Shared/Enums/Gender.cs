namespace Nova.Shared.Enums;

/// <summary>
/// Defines gender values that may be associated with a player profile.
/// </summary>
public enum Gender
{
    /// <summary>
    /// Indicates a male gender value.
    /// </summary>
    Male,

    /// <summary>
    /// Indicates a female gender value.
    /// </summary>
    Female,

    /// <summary>
    /// Indicates a gender value outside of male or female selections.
    /// </summary>
    Other
}
